
const marks = 94 ;

if(marks >= 80 && marks <= 100){
    console.log("Grade is A+");
}
else if(marks >= 70 && marks <= 79){
    console.log("Grade is A");
}
else if(marks >= 60 && marks <= 69){
    console.log("Grade is A-");
}
else if(marks >= 50 && marks <=59){
    console.log("Grade is B");
}
else if(marks >= 40 && marks <= 49){
    console.log("Grade is C");
}
else{
    console.log("Fail");
}