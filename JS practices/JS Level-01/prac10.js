// push => add element in last

let numbers = [23, 45, 66, 67];

console.log(numbers);

numbers.push(68);

console.log(numbers);

// pop => remove element in last

numbers.pop();

console.log(numbers);

// shift => remove element in first

let frnds = ['Karim', 'Rahim'];

console.log(frnds);

frnds.shift();

console.log(frnds);

// unshift => add element in first

frnds.unshift('Roshan');

console.log(frnds);